import Foundation
import SwiftUI

extension Color {
    static var backgroundColor: Color {
        return Color("backgroundGrey")
    }
    
    static var lighterGreen: Color {
        return Color("lighterGreen")
    }
    
    static var darkerGreen: Color {
        return Color("darkerGreen")
    }
    
    static var darkerOrange: Color {
        return Color("darkerOrange")
    }
    
    static var lighterOrange: Color {
        return Color("lighterOrange")
    }
    
    static var lighterGrey: Color {
        return Color("lighterGrey")
    }
}
